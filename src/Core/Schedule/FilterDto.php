namespace Swoolefy\Core\Schedule;

class FilterDto {

    public $fn = null;

    public $params = [];

    public function setFn($fn)
    {
        $this->fn = $fn;
    }

    public function getFn()
    {
        return $this->fn;
    }

    public function setParams(array $params)
    {
        $this->params = $params;
    }

    public function getParams()
    {
        return $this->params;
    }
}